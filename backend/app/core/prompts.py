from langchain_core.prompts import ChatPromptTemplate, PromptTemplate

# ============================================================
# 基本プロンプトテンプレート
# ============================================================

SYSTEM_PROMPT_BASE = """あなたは優秀なエンジニアリングアドバイザーです。
ユーザーのスキルレベルと目標に応じて、適切なアドバイスと課題を提供します。"""


# ============================================================
# ランク判定用プロンプト（Phase 3で詳細実装）
# ============================================================

RANK_ANALYSIS_TEMPLATE = PromptTemplate(
    input_variables=["github_data", "portfolio_data", "external_data"],
    template="""# エンジニアスキル判定

以下の情報を元に、このユーザーのエンジニアとしてのスキルレベルを
全エンジニアの中での相対位置（パーセンタイル）で推定してください。

## GitHub実績
{github_data}

## ポートフォリオ
{portfolio_data}

## 外部活動
{external_data}

## 判定基準
- リポジトリの質（README、コミット履歴、コード品質）
- 使用技術の幅と深さ
- コミュニティ貢献
- 継続的な学習姿勢

**出力形式**:
パーセンタイル: [0-100の数値のみ]
理由: [簡潔な説明]
""",
)


# ============================================================
# スキルツリー生成用プロンプト（Phase 3で詳細実装）
# ============================================================

SKILL_TREE_TEMPLATE = ChatPromptTemplate.from_messages(
    [
        ("system", SYSTEM_PROMPT_BASE),
        (
            "user",
            """# スキルツリー生成

ユーザー情報:
- 現在のランク: {current_rank}
- カテゴリ: {category}
- 習得済みスキル: {acquired_skills}

このユーザーが次のランクに進むために必要なマイルストーンを
JSON形式で生成してください。

出力形式:
{{"nodes": [{{"id": 1, "skill": "スキル名", "acquired": true/false}}]}}
""",
        ),
    ]
)


# ============================================================
# 演習生成用プロンプト（Phase 3で詳細実装）
# ============================================================

QUEST_GENERATION_TEMPLATE = ChatPromptTemplate.from_messages(
    [
        ("system", SYSTEM_PROMPT_BASE),
        (
            "user",
            """# ハンズオン演習生成

ドキュメント内容:
{document_content}

対象ユーザー:
- ランク: {user_rank}
- 得意分野: {user_skills}

このドキュメントの内容を学ぶための、実践的なハンズオン演習を生成してください。

## 要件
- ユーザーのランクに適した難易度
- 段階的な手順（5-10ステップ）
- 実行可能な具体的なタスク
- 学習効果の高い内容

出力形式: Markdown
""",
        ),
    ]
)


# ============================================================
